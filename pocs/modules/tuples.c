int tuples_len = sizeof(tuples) / sizeof(tuples[0]);

struct tuple *find_tuple(char *ascii) {
    int i = 0;
    while (i < tuples_len && 
        strcmp(tuples[i].ascii, ascii) != 0
    ) {
        i++;
    }
    int found = i < tuples_len;
    assert(found);
    return &tuples[i];
}
